const work = (lines) => {
  let result = 0;
  for (line of lines) {
    line.trim().split(' | ').pop().split(' ').forEach(number => {
      if (number.length === 2 || number.length === 3 || number.length === 4 || number.length === 7) result++;
    })
  }
  return result;
}

